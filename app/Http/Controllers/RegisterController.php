    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $cat = CategoriaClassificados::latest()->get();
        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'lowercase', 'email', 'max:255', 'unique:' . User::class],
            'password' => ['required', 'confirmed', Rules\Password::defaults()],
        ]);

        User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'role' => 2
        ]);
        if ($request->check1 == 1) {
            return view('home.pages.payment.01', compact('cat'));
        }
        if ($request->check2 == 2) {
            return view('home.pages.payment.02', compact('cat'));
        }
        if ($request->check3 == 3) {
            return view('home.pages.payment.03', compact('cat'));
        }
        if ($request->check4 == 4) {
            return view('home.pages.payment.04', compact('cat'));
        }
    }

